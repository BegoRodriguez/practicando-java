class Switch {
    public static void main(String[] args) {
       

        int entero = 0;

        switch (entero) {
            case 0:
                System.out.println("Hola Soy un cero");

            case 1:
                System.out.println("Hola Soy un uno");
                break;

            case 2:
                System.out.println("Hola Soy un dos");
                break;
        
            default:
                System.out.println("Hola");
                break;
        }
    }

         



    }